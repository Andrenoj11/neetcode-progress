class Solution {
    /**
     * @param {number[]} numbers
     * @param {number} target
     * @return {number[]}
     */
    twoSum(numbers, target) {
       let l = 0;
    let r = numbers.length - 1;

    while (l < r) {
      const sum = numbers[l] + numbers[r];

      if (sum === target) return [l + 1, r + 1];
      if (sum < target) l++;
      else r--;
    }

    // Karena dijamin selalu ada 1 solusi, baris ini "secara teori" tidak akan kepakai
    return [];
    }
}
